pub const HEADER_NAME: &str = "bloom-response-buckets";

pub fn parse_buckets(value: &str) -> Vec<String> {
    value
        .split(',')
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty())
        .collect()
}
